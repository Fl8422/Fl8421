        bot.send_message(message.chat.id, f"–ü–µ—Ä–µ–ø–ª–∞–≤–∫–∞ {amount} x {ore} –Ω–∞ –ø–ª–∞–Ω–µ—Ç–µ {current_planet} –∑–∞–≤–µ—Ä—à–µ–Ω–∞.")
